
public class Vertice {

	public String cor ="braca";
	public int distancia = 0;
	public int vertice = 0;
	public int hasVertice= 0;
	
	public Vertice(){}
	
	public Vertice (int d, String c, int v, int h) {
		this.distancia = d;
		this.cor = c;
		this.vertice = v;
		this.hasVertice = h;
	}
}
